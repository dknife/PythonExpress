# 코드 3.7: if-elif-else 조건문으로 학점 판정  —  grade_test.py

score = int(input('Enter score: '))
if score >= 90:
    grade = 'A'
elif score >= 80:
    grade = 'B'
elif score >= 70:
    grade = 'C'
elif score >= 60:
    grade = 'D'
else:
    grade = 'F'
print(f'Grade: {grade}')
